								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>MEDICAL OFFICE COMPUTER APPLICATIONS:</strong></h5>
										<p>MCI 140 - 30 clock hours</p>
										<p>Lecture and laboratory experiences will introduce the learner to microcomputer applications unique to the medical office. Applications will include the following computerized tasks: establishing information files, appointments, patient account information, accounts receivable, aging accounts, insurance billing, recall notice, and production reports.</p>
