									<div class="icon-content">
										<h5 class="dez-tilte"><strong>BASIC X-RAY TECHNICQUES:</strong></h5>
										<p>MCI 145 - 30 clock hours</p>
										<p>Designed for medical assistants and other health care workers who assist with radiographic procedures in ambulatory care facilities. This lecture/lab course focuses upon the basic components of radiation protection, radiation safety, basic radiologic positioning, fundamental procedures, equipment, special  studies, darkroom procedure as well as film processing.</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>MEDICAL TERMINOLOGY I</strong></h5>
										<p>MCI 201 - Lec - 40 clock hours</p>
										
										<p>This course will introduce students to basic medical terminology through an orientation to root words, prefixes and suffixes on a system-by system basis.  Students will develop the ability to identify and analyze terminology used in health care literature. </p>
										<p><strong>Prerequisites:</strong>  None</p>
									</div>
								</div>
							</div>
                      
