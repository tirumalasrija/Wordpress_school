								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Uniform billing</strong></h5>
										<p>MCI 210 - Lec - 30 clock hours</p>
										<p>This course was designed to enable students working with form locators, acute care facility, internal network of a hospital, exploring covered services and rates, coding and reimbursement system. </p>
										<p><strong>Prerequisite:</strong>  MBI 206 </p>
									</div>
								</div>
							</div>
                     
