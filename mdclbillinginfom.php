								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Medical Terminology II</strong></h5>
										<p>MCI 202 - Lec - 40 clock hours</p>
										<p>In this class students will learn the importance of correct spelling and pronunciation of medical terms.  Students will acquire entry-level skills in the interpretation of medical terminology.</p>
										<p><strong>Prerequisites:</strong> Medical Terminology I</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>MEDICAL FINANCES & INSURANCE</strong></h5>
										<p>MCI 203 - Lec - 40 clock hours</p>
										<p>This course is an overview of the billing practices found in medical offices and students will use these practices as a focus for learning.  Students will be introduced to appropriate insurance and billing terminology and tasks that are the responsibility of the medical insurance specialist. Students will also demonstrate an understanding of the various private and public medical and disability insurance as well as the various reference materials available.  Emphasis will be on professional responsibility and accuracy. </p>
										<p><strong>Prerequisite:</strong> MBI 202 </p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>PHYSICIAN BILLING:</strong></h5>
										<p>MCI 204 - Lec - 40 clock hours</p>
										<p>This course provides an introduction to the application of the computer in physician billing for health care carriers such as: Blue Cross/Blue Shield, HMO’s, Medicare, Commercial, Worker’s Compensation, Disability, and the Federal Employees’ Program.  Students will use a current physician software package designed to promote proficiency in the creation of test cases in billing.  The course provides a unique hands-on approach in which students will learn all formats and techniques in claims processing and generating patient billing statements.    </p>
										<p><strong>Prerequisite:</strong> MBI 202 </p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>HOSPITAL BILLING:</strong></h5>
										<p> MCI 205 - Lec - 40 clock hours</p>
										<p>This course provides an introduction to the application of the computer in facility claims processing.  Students will use a current hospital software package designed to promote proficiency in the creation of test cases in billing.  The course provides unique hands-on approach in which students will learn all formats and techniques necessary in claims processing for hospitals, nursing homes, free standing facilities, and rehabilitation centers.</p>
										<p><strong>Prerequisite:</strong> MBI 202  </p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Medical Diagnostic & Procedural Coding</strong></h5>
										<p>MCI 206 - Lec - 40 clock hours</p>
										<p>This course provides an overview of medical coding for diagnoses and procedural service.  Students will be introduced to the various resources and references utilized for coding issues, including the International Classification of Diseases, 9th  Revision, Clinical Modification (ICD-9, CM), and Physicians Current Procedural Terminology (CPT) and Health Care Financing Administration Common Procedure Coding System (HCPCS).  Students will also learn the importance of appropriate linkage of diagnostic codes to procedure codes, supporting medical documentation, use of modifiers and global packages. </p>
										<p><strong>Prerequisite:</strong>  MBI 204</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Claim Review Management</strong></h5>
										<p>MCI 207 - Lec - 40 clock hours</p>
										<p>This course provides an in-depth study of health claims examining/review, procedural coding (appropriate use of bundling) and reimbursement.  Students will be introduced to medical billing concepts pertaining to various physician medical specialties and allied health professions including anesthesia, radiology, laboratory, and durable medical equipment.</p>
										<p><strong>Prerequisite:</strong>   MBI 204</p>
									</div>
								</div>
							</div>
                      
