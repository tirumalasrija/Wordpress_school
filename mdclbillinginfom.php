										<p>MCI 110 - 30 Clock hours</p>
										<p><h3>Clinical:</h3>  An advanced course designed for students admitted to the program, includes the medical assisting principles and procedures specifically related to the clinical area, such as vital signs, administration of medications, venipuncture, medical and surgical asepsis, assisting with examinations and treatments, lectrocardiography, routine diagnostic laboratory tests, and medical emergencies. </p>
										
										<h5 class="dez-tilte">MEDICAL OFFICE ASSISTANT PROCEDURES:</h5>
										<p>MCI 120 - 30 Clock hours</p>
										<p><h3>Administrative:</h3>  Serves as the foundation course for the Medical Assistant and Medical Receptionist and Medical Insurance Specialist programs. This course presents the administrative and office skills needed for understanding the significance of membership in a service profession. Topics included are personal qualifications,
employability skills, ethical/legal responsibilities, receptionist duties, scheduling appointments, telephone techniques, filing, maintaining patient records, billing and collecting fees, bookkeeping, and computer </p>
