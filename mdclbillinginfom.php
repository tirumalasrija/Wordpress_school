										<h5 class="dez-tilte"><strong>Patient Accounting Medisoft Software</strong></h5>
										<p>MCI 212 - Lec - 40 clock hours</p>
										<p>This course was designed to acknowledge students with Medisoft software as a patient accounting medical billing software.  Students will be able to install and working with the program and its components. Student will learn the understanding the program, setting up the practice, patient setup, transaction entry, claim management, statement and reports, Medisoft report designer, and office hours patient appointment schedule.  </p>
										<p><strong>Prerequisite:</strong>  Claim Review Management</p>
