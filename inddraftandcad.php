                        <h1 class="m-b20">Industrial drafting & CAD (AUTO CAD)</h1>
                        <div class="dez-separator bg-primary"></div>
                        
                        <div class="row">
                        	<div class="col-md-12 col-sm-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>INDUSTRIAL DRAFTING & CAD TECHNOLOGY</strong> 700 clock hours </h5>
										<p>18 hours/ week -  39 weeks</p>
										<p><h3>OBJECTIVES:</h3>  Applying AutoCAD is not restricted to one discipline.  Rather, it serves all areas that require methods of drafting, design, and engineering.  These areas include architecture, civil engineering, mapping, landscaping, electricity/electronics, mechanical, product design, tooling design, structural engineering, facilities planning, and interior design.  Less common but potentially productive areas include theater set and lighting design, museum display design, and even archaeology.</p>
										
										<h3>CAREER OPPORTUNITIES:</h3>
										<p>The curriculum is designed to give students experience in a wide variety of drafting specialties.  Included in the program are layout and detailing in product design, machine element drafting, fixture design, and dire design.  Upon completion of the program, students are ready for an entry level in areas that offer the broadest career opportunities, such as Detailer, CAD Operator, Mechanical Drafter, Cost Estimator, Expeditor, Plant Layout Specialist, Electronic Drafter, and Technical Salesperson.</p>
									</div>
								</div>
							</div>
                       </div>
                        
                        <div class="row">
                        	<div class="col-md-12 col-sm-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Keyboarding/Typing:</strong></h5>
										<p>OIS 101 - Lab - 40 clock hours</p>
										<p>This course is designed to instruct the beginning student in the use of proper techniques to master the microcomputer keyboard.  Students learn the proper techniques of alphanumeric keyboarding and the ten-key numeric keypad.  Minimum speed requirement is 20 net words per minute on three timed writings with a three errors limit.  </p>
									</div>
								</div>
							</div>
                       </div>
                       
                       
                       <div class="row">
                        	<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>COMPUTER FUNDAMENTALS </strong></h5>
										<p>CIS 102 - Lec - 40 clock hours</p>
										<p>This course develops a student’s understanding of information systems.  Students learn the basic principles, concepts and terminology of computers and computer information systems.  Students develop knowledge regarding computer systems and related equipment.  Information about computer careers such as operations, programming and system design is discussed along with future trends in the computer industry.  Students will also introduced to Microsoft Windows and Internet access.</p>
										<p><strong>Prerequisite:</strong> None</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										
										<p>Windows Family - CIS 103 - 40 clock hours</p>
										
										<p>This class is designed to provide the updated development of Windows family.  It includes using explorer, hardware, software, peripherals, customizing the desktop, and customizing taskbar.  Student will be able to work with file and storage management, creating and using shortcuts, and design computer display.  Student will be able also to view all the functions of Windows family, such as transferring data between applications and mouse tips.  </p>
										<p><strong>Prerequisites:</strong>  None</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Medical Terminology II</strong></h5>
										<p>MCI 202 - Lec - 40 clock hours</p>
										<p>In this class students will learn the importance of correct spelling and pronunciation of medical terms.  Students will acquire entry-level skills in the interpretation of medical terminology.</p>
										<p><strong>Prerequisites:</strong> Computer Fundamentals</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Fundamentals of Arithmetic</strong></h5>
										<p>MATH 070 - Lec - 40 clock hours</p>
										<p>A developmental course for students who need instruction in the basic concepts of arithmetic.  Topics covered include operations with whole number, fractions and decimals, percentage, ratio and proportion, and measurement.  Techniques of problem-solving and applications are included through the course.  Prerequisites:  No computer experience is required.</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Basic Technical Mathematics</strong></h5>
										<p>MATH 100 - Lec - 40 clock hours</p>
										<p>This course was designed to acknowledge the students with the topics that include a review of arithmetic, an introduction to the scientific calculator, approximate numbers, dimensional analysis, beginning algebra, geometry, and statistics.  This course emphasizes practical technical applications.    </p>
										<p><strong>Prerequisite:</strong> MATH 070 </p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Introduction to Graphical Interpretation</strong></h5>
										<p> CAD 100 - Lec - 40 clock hours</p>
										<p>This course is print reading and interpretation for the non-drafting student of the student wishing to explore technical drafting.  This course introduces the student to basic projection, dimensioning, and conventions that compromise an industrial drawing.  Students develop the ability to interpret blueprints of manufacturing equipment and customer products.</p>
										<p><strong>Prerequisite:</strong> Computer Fundamentals. </p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Introduction to Industrial Drafting</strong></h5>
										<p>CAD 110 - Lec - 40 clock hours</p>
										<p>This course is a drafting course for the student who plas to pursue the program.  Units of instructions include the use of drafting instruments, lettering technique, geometric construction, orthographic project, pictorial drawing, basic dimensioning, sectioning, and auxiliary.</p>
										<p><strong>Prerequisite:</strong>  CAD 100</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Introduction to CAD</strong></h5>
										<p> CAD 120 - Lec - 40 clock hours</p>
										<p>This course is a basic computer-aided drafting class for the student planning to pursue the CAD program.  Topics considered are general computer operations, geometric construction, construction of two –dimensional orthographic drawings including auxiliaries and sections, and dimensioning.</p>
										<p><strong>Prerequisite:</strong>   CAD 110</p>
									</div>
								</div>
							</div>
                      
                      		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Introduction to CATIA</strong></h5>
										<p>CAD 123 - Lec - 30 clock hours</p>
										<p>The students will be introduced to CATIA and its functionality.  Students will use the Part Design, Drafting, Assembly Design, and Generative Shape Design Work Benches to make models that are properly constructed and constrained.  CATIA’s tools and tool bars will be demonstrated and used in the various Work Benches.  </p>
										<p><strong>Prerequisite:</strong> Introduction to CAD</p>
									</div>
								</div>
							</div>
                     
                     		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Technical Descriptive Geometry</strong></h5>
										<p> CAD 125 - Lec - 30 clock hours</p>
										<p>An intermediate-level drafting course in advanced projection techniques.  Auxiliary views are used to manipulate geometry to define the relationship between points, lines, planes, and solids.  This course also includes topics on revolution, intersections, solids and developments.  </p>
										<p><strong>Prerequisite:</strong>  CAD 110</p>
									</div>
								</div>
							</div>
                    
                    	<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Industrial Detailing</strong></h5>
										<p>CAD 130 - Lec - 40 clock hours</p>
										<p>This course includes the drawing of necessary view, placement of dimensions, dimensioning styles, and calculation of tolerances.  The function and relationship of mating parts of an assembly are considered when dimensioning to insure proper fit.  Threads, fasteners, and common manufacturing operations are applied and dimensioned.  Standard and commercial parts are selected through catalogs.  Setup and application of CAD dimensioning styles and tolerances are utilized.  </p>
										<p><strong>Prerequisite:</strong>  CAD 110</p>
									</div>
								</div>
							</div>
                    
                    		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Die Design</strong></h5>
										<p>CAD 140 - Lec - 40 clock hours</p>
										<p>An advanced drawing and process-oriented course in the layout and design of production press work dies.  Typical dies covered are lank, cam pierce, form cutoff, draw, and progressive.  Press computations and accessories are put in perspectives and they relate to design problems.  Drawing assignments are done manually and on CAD . </p>
										<p><strong>Prerequisite:</strong>  CAD 125</p>
									</div>
								</div>
							</div>
                    
                    		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Machine Element Drafting</strong></h5>
										<p>CAD 145 - Lec - 40 clock hours</p>
										<p>This course is an advanced course providing and insight into the study of mechanisms, their motion, and related skeletal construction.  Calculations are made to determine size and capacity requirements of machine elements and started parts.  Industrial techniques are applied to detail drawing of various machine parts.  Drawing assignment are done manually and on CAD. </p>
										<p><strong>Prerequisite:</strong>  CAD 130</p>
									</div>
								</div>
							</div>
                    
                    		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Jigs, Fixtures, Tools</strong></h5>
										<p>CAD 150 - Lec - 40 clock hours</p>
										<p>This course is an advanced process-oriented drafting course in design, layout, and detail of production tooling.  Consideration is given to locating, clamping, and tolerance a jig and fixture for the manufacture of an industrial part.  Standard parts catalogs and library reference material provide guidance in solving design problems.  Drawing assignments are done manually and on CAD.</p>
										<p><strong>Prerequisite:</strong>  CAD 125, CAD 130</p>
									</div>
								</div>
							</div>
                    
                    		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Product Drawing</strong></h5>
										<p>CAD 160 - Lec - 40 clock hours</p>
										<p>This course is an advanced drawing course that examines the demands of product drafting.  The design process is applied to develop a product with consideration of its function and ability to be economically manufacturer.  Drawing assignments are done manually and on CAD.</p>
										<p><strong>Prerequisite:</strong>  CAD 125, CAD 130</p>
									</div>
								</div>
							</div>
                    
                    		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>CAD-Advanced Techniques</strong></h5>
										<p>CAD 165 - Lec - 40 clock hours</p>
										<p>This course is a CAD course developing skill in the advanced operations of making two-and three-dimensional models.  Solids analysis and the manipulation of geometry using auxiliary views are practiced.  Concepts of descriptive geometry and detailing are reviewed and developed as they relate to model geometry.</p>
										<p><strong>Prerequisite:</strong>  CAD 110, CAD 120 or concurrent enrollment in CAD 125 and CAD 130</p>
									</div>
								</div>
							</div>
                    
                    		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Advanced CAD Applications – Solid Modeling</strong></h5>
										<p> CAD 170 - Lec - 40 clock hours</p>
										<p>A course designed to incorporate three-dimensional, feature-based parametric solid concepts.  This course allows the student to create complex three-dimensional parametric models and then general two-dimensional views from those CAD models.  Single part and assembly modeling concepts are developed and applied to a variety of simple and complex CAD models.</p>
										<p><strong>Prerequisite:</strong>  CAD 110, CAD 120 or concurrent enrollment in CAD 125 and CAD 130</p>
									</div>
								</div>
							</div>
                    
                    		<div class="col-md-12">
								<div class="icon-bx-wraper bx-style-1 p-a20 left m-b30">
									<div class="icon-content">
										<h5 class="dez-tilte"><strong>Architecture Drafting & Design</strong></h5>
										<p> CAD 180 - Lec - 40 clock hours</p>
										<p>This course provides students with cutting-edge information on computer aide-design, energy-efficient design, structural calculations, solar planning, and site development.  Its logical organizational structure permits flexibility to present instructions in variety of sequences.</p>
										<p><strong>Prerequisite:</strong>  Computer Fundamentals</p>
