										<p>CNS 208 - 40 clock hours</p>
										<p>The course starts with an unknown database that requires tuning. The lessons will proceed through the steps a DBA will perform to acquire the information needed to identify problem areas, to diagnose common problems, and remedy those problems. The methodology used in the practices is primarily reactive. After configuring monitoring tools, and reviewing the available reports, the student will be presented with the Oracle architecture based on the SQL statement processing of SELECT and DML. The SQL tuning section assumes that the DBA has little or no ability to change the code. The DBA will influence the SQL performance with available tools. The DBA will be introduced to various methods of identifying the SQL statements that require tuning, and the diagnostic tools needed to find ways to change the performance. This will include the use of statistics, outlines, and profiles to influence the optimizer, adding and rebuilding indexes, and using the SQL Advisors. A major task of DBA’s is to maintain SQL performance across changes. This course introduces the DB Replay, and SQL Performance Analyzer tools to help the DBA test and minimize the impact of change. </p>
