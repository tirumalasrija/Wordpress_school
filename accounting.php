										<p>This class continues the study of accounting principles with special emphasis on partnerships and corporations.  Students apply basic accounting theory and procedures to partnership, corporation, long-term asset, and liability accounting.  Students also learn computerized accounting procedures for operational assets, payroll and related liabilities, equity investments, and financial statement analysis.   </p>
