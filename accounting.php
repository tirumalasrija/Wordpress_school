										<p>OIS 391 - Lec - 40 clock hours</p>
										<p>This course continues the study of federal taxation, focusing primarily on income taxes for partnerships and corporations but also reviewing social security, estate and gift taxes.  Students learn federal income tax laws pertaining to partnerships and corporations, federal tax laws applying to estates, trusts, and gifts.  Students also learn to perform research and to prepare appropriate tax returns, including computerized returns.</p>
