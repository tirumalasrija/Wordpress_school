										<p>This course briefly reviews fundamental accounting processes and provides a detailed study of the major categories of current assets.  Students learn the accounting cycle; cash and receivables; future and present value concepts; the balance sheet; cash flow and income statements; and short-term investments.  Students also study inventory, applicable APB and FASB pronouncements and related topics. </p>
